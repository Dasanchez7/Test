//Funciones\
// Declaracionn de funcion que recibe dos parametros
function sumar(a,b) {
    let resultadosuma = a+b;
    return resultadosuma;
}

// se almacena el resultado de la funcion llamandola
//junto con dos argumentos
let argA = 5, argB = 7;
let resultado = sumar(1,2);

// funciones son aquellas que retornan valores
// de lo contrario es un procedimiento
console.log(resultado);

resultado = sumar (argA,argB);
console.log(resultado);

