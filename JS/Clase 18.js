// Operadores aritmeticos

let x = 1;
let y = 2; 

// suma
console.log(x+y);
// resta
console.log(x-y);
// multiplicacion
console.log(x*y);
// division
console.log(x/y);
// residuo de la division
console.log(x%y);

// preincremento
console.log(++x);
// posincremento
console.log(x++);
console.log(x)

//potencia
console.log(y**3);


